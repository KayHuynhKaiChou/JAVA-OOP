public class Course {
    private double fee;
    private String name;

    public Course(double fee, String name) {
        this.fee = fee;
        this.name = name;
    }

    public double getFee() {
        return fee;
    }

    public void setFee(double fee) {
        this.fee = fee;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
